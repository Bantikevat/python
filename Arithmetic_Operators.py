# Operators: + (Addition), - (Subtraction), * (Multiplication), / (Division),
#            % (Modulus), ** (Exponentiation), // (Floor Division)

# 1Ô∏è‚É£ Addition (+) - ‡§ú‡•ã‡§°‡§º
# Example: ‡§¶‡•ã ‡§∏‡§Ç‡§ñ‡•ç‡§Ø‡§æ‡§ì‡§Ç ‡§ï‡§æ ‡§ú‡•ã‡§°‡§º
num1 = 10
num2 = 20
sum_result = num1 + num2
print(f"Addition: {num1} + {num2} = {sum_result}")

# 2Ô∏è‚É£ Subtraction (-) - ‡§ò‡§ü‡§æ‡§µ
# Example: ‡§¨‡•à‡§Ç‡§ï ‡§Ö‡§ï‡§æ‡§â‡§Ç‡§ü ‡§¨‡•à‡§≤‡•á‡§Ç‡§∏ ‡§∏‡•á ‡§™‡•à‡§∏‡•á ‡§®‡§ø‡§ï‡§æ‡§≤‡§®‡§æ
balance = 5000
withdraw = 1200
remaining_balance = balance - withdraw
print(f"Remaining Balance: {balance} - {withdraw} = {remaining_balance}")

# 3Ô∏è‚É£ Multiplication (*) - ‡§ó‡•Å‡§£‡§æ
# Example: ‡§ï‡§ø‡§∏‡•Ä ‡§∏‡§æ‡§Æ‡§æ‡§® ‡§ï‡•Ä ‡§ï‡•Å‡§≤ ‡§ï‡•Ä‡§Æ‡§§ ‡§®‡§ø‡§ï‡§æ‡§≤‡§®‡§æ
price_per_item = 50
quantity = 5
total_price = price_per_item * quantity
print(f"Total Price: {price_per_item} * {quantity} = {total_price}")

# 4Ô∏è‚É£ Division (/) - ‡§≠‡§æ‡§ó
# Example: ‡§ü‡•Ä‡§Æ ‡§Æ‡•á‡§Ç ‡§∏‡§Æ‡§æ‡§® ‡§∞‡•Ç‡§™ ‡§∏‡•á ‡§ö‡•â‡§ï‡§≤‡•á‡§ü ‡§¨‡§æ‡§Å‡§ü‡§®‡§æ
total_chocolates = 10
members = 3
chocolates_per_member = total_chocolates / members
print(f"Each member gets: {chocolates_per_member} chocolates")

# 5Ô∏è‚É£ Modulus (%) - ‡§∂‡•á‡§∑‡§´‡§≤ ‡§®‡§ø‡§ï‡§æ‡§≤‡§®‡§æ
# Example: ‡§ï‡§ø‡§∏‡•Ä ‡§∏‡§Ç‡§ñ‡•ç‡§Ø‡§æ ‡§ï‡•ã ‡§ú‡§æ‡§Å‡§ö‡§®‡§æ ‡§ï‡§ø ‡§Ø‡§π ‡§∏‡§Æ (even) ‡§π‡•à ‡§Ø‡§æ ‡§µ‡§ø‡§∑‡§Æ (odd)
num = 7
if num % 2 == 0:
    print(f"{num} is Even")
else:
    print(f"{num} is Odd")

# 6Ô∏è‚É£ Exponentiation (**) - ‡§ò‡§æ‡§§‡§æ‡§Ç‡§ï
# Example: ‡§ï‡§ø‡§∏‡•Ä ‡§∏‡§Ç‡§ñ‡•ç‡§Ø‡§æ ‡§ï‡§æ ‡§∏‡•ç‡§ï‡•ç‡§µ‡§æ‡§Ø‡§∞ ‡§Ø‡§æ ‡§ï‡•ç‡§Ø‡•Ç‡§¨ ‡§®‡§ø‡§ï‡§æ‡§≤‡§®‡§æ
base = 2
power = 3
result = base ** power
print(f"{base} raised to power {power} = {result}")

# 7Ô∏è‚É£ Floor Division (//) - ‡§≠‡§æ‡§ó‡§´‡§≤ ‡§ï‡§æ ‡§™‡•Ç‡§∞‡•ç‡§£‡§æ‡§Ç‡§ï ‡§≠‡§æ‡§ó
# Example: ‡§ï‡§ø‡§∏‡•Ä ‡§∏‡§Ç‡§ñ‡•ç‡§Ø‡§æ ‡§ï‡•ã ‡§ó‡•ç‡§∞‡•Å‡§™ ‡§Æ‡•á‡§Ç ‡§¨‡§æ‡§Å‡§ü‡§®‡§æ ‡§î‡§∞ ‡§™‡•Ç‡§∞‡•ç‡§£ ‡§ó‡•ç‡§∞‡•Å‡§™ ‡§®‡§ø‡§ï‡§æ‡§≤‡§®‡§æ
